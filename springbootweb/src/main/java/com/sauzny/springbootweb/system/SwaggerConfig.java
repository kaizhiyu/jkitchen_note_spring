@Configuration
@EnableSwagger2
@ConditionalOnExpression("${swagger.enabled:true}")
public class SwaggerConfig {

    @Bean
    public Docket createRestApi() {
        return new Docket(DocumentationType.SWAGGER_2).apiInfo(apiInfo()).select()
                .apis(RequestHandlerSelectors.basePackage("com.sauzny.springbootweb.controller"))
                .apis(RequestHandlerSelectors.withClassAnnotation(Api.class))
                .apis(RequestHandlerSelectors.withMethodAnnotation(ApiOperation.class))
                .paths(PathSelectors.any())
                .build();
    }

    private ApiInfo apiInfo() {
        return new ApiInfoBuilder()
                .title("spring boot web 系统 api")
                .description("spring boot web 系统 api")
                .contact(new Contact("sauzny", "", "xinxin_949421@126.com"))
                .version("1.0")
                .build();
    }

}
